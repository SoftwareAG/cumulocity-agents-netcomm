def setCreds():
  try:
    os.environ['AWS_ACCESS_KEY_ID'] = raw_input("Set the AWS Access Key ID: ")
    os.environ['AWS_SECRET_ACCESS_KEY'] = raw_input("Set the AWS Secret Access Key: ")
  except KeyboardInterrupt:
    print
    colorize('red')
    logging.info("Execution aborted..")
    colorize('neu')
