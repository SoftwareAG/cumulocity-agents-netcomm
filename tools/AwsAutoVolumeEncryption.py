def checkVars(*variables):
  for v in variables:
    if not v in globals() or globals()[v] is None or globals()[v] == [] or globals()[v] == {}:
      return False
  return True

def saveState(statefile, id, content, trailCom=True):
  if not checkOnly:
    trail = ',' if trailCom else ''
    f = open(statefile,"a")
    f.write('"' + id + '": ' + json.dumps(content,indent=4) + trail + "\n")
    f.close()

def startState(statefile):
  if not checkOnly:
    f = open(statefile,"w+")
    f.write('{\n')
    f.close()

def closeState(statefile):
  if not checkOnly:
    f = open(statefile,"a")
    f.write('}\n')
    f.close()

def forceConfirm(question):
  answer = preAnswer
  try:
    while answer != 'yes':
      answer = raw_input(question + "\nType 'yes': ")
  except KeyboardInterrupt:
    print
    colorize('red')
    logging.info("Execution aborted..")
    colorize('neu')
    sys.exit()

def askConfirm(question):
  answer = preAnswer
  try:
    while answer != 'yes' and answer != 'no':
      answer = raw_input(question + "\nType 'yes' or 'no': ")
    if answer == 'yes':
      return True
    elif answer == 'no':
      return False
  except KeyboardInterrupt:
    print
    colorize('red')
    logging.info("Execution aborted..")
    colorize('neu')
    sys.exit()

def retry(func, retries=4, **kwargs):
  logging.debug("retry function triggered")
  count = 0 
  while True:
    try:
      func(**kwargs)
    except KeyboardInterrupt:
      print
      colorize('red')
      logging.info("Execution aborted..")
      colorize('neu')
      sys.exit()
    except:
      colorize('ylw')
      logging.warning("First attempt failed...")
      colorize('neu')
      count+=1
      if count <= retries:
        colorize('ylw')
        logging.warning("Trying once more (" + str(count) + "/" + str(retries) + ")")
        colorize('neu')
        continue
      else:
        colorize('red')
        logging.error("Reached maximum retries and failed")
        colorize('neu')
    break


